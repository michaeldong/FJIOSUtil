#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    // Navigation logic may go here. Create and push another view controller.
    /*
     <#DetailViewController#> *detailViewController = [[<#DetailViewController#> alloc] initWithNibName:@"<#Nib name#>" bundle:nil];
     // ...
     // Pass the selected object to the new view controller.
     [self.navigationController pushViewController:detailViewController animated:YES];
     [detailViewController release];
     */
  
  
  FJProgressHUD *hud;
  switch (indexPath.row) {
    case 0:
      hud = [FJProgressHUD ProgressHUDWithSize:CGSizeMake(150, 150)];
      [hud setIconType:kFJProgressHUDIconActivity];
      break;
      
    case 1:
      hud = [FJProgressHUD ProgressHUDWithSize:CGSizeMake(150, 100)];
      [hud setIconType:kFJProgressHUDIconActivity];
      [hud setMessageText:@"Loading..."];
      break;
      
    case 2:
      hud = [FJProgressHUD ProgressHUDWithSize:CGSizeMake(150, 120)];
      hud.contentInsets = UIEdgeInsetsMake(20.f, 5.f, 5.f, 5.f);
      [hud setIconType:kFJProgressHUDIconActivity];
      [hud setMessageText:@"Loading..."];
      [hud setLayoutType:kFJProgressHUDLayoutVertical];
      break;
      
    default:
      break;
  }
   
  
  [hud showHUDInView:self.view modal:YES];
             
}

